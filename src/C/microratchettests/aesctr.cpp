static mr_config _cfg{ 1000, 1 };

TEST(AesCtr, Init256) {
	const unsigned char key[]{
		0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
		0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
	};
	const unsigned char iv[]{
		0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F
	};

	auto mr_ctx = mrclient_create(&_cfg);
	auto aes = mr_aes_create(mr_ctx);
	_mr_aesctr_ctx aesctr;
	EXPECT_NE(nullptr, aes);
	int result = mr_aes_init(aes, key, (unsigned int)sizeof(key));
	EXPECT_EQ(E_SUCCESS, result);
	result = aesctr_init(&aesctr, aes, iv, (unsigned int)sizeof(iv));
	EXPECT_EQ(E_SUCCESS, result);

	mr_aes_destroy(aes);
	mrclient_destroy(mr_ctx);
}

