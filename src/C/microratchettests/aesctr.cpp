void processTestInternal(
	const unsigned char* key, unsigned int keysize,
	const unsigned char* iv, unsigned int ivsize,
	const unsigned char* input, unsigned int inputsize,
	const unsigned char* expected, unsigned int expectedsize)
{
	unsigned char* output = new unsigned char[inputsize];
	unsigned int outputsize = inputsize;

	auto mr_ctx = mrclient_create(&_cfg);
	auto aes = mr_aes_create(mr_ctx);
	_mr_aesctr_ctx aesctr;
	EXPECT_NE(nullptr, aes);
	int result = mr_aes_init(aes, key, keysize);
	EXPECT_EQ(E_SUCCESS, result);
	result = aesctr_init(&aesctr, aes, iv, ivsize);
	EXPECT_EQ(E_SUCCESS, result);

	result = aesctr_process(&aesctr, input, inputsize, output, outputsize);
	EXPECT_EQ(E_SUCCESS, result);
	EXPECT_BUFFEREQ(output, outputsize, expected, expectedsize);

	mr_aes_destroy(aes);
	mrclient_destroy(mr_ctx);
	delete[] output;
}

void processTest(
	const unsigned char* key, unsigned int keysize,
	const unsigned char* iv, unsigned int ivsize,
	const unsigned char* input, unsigned int inputsize,
	const unsigned char* expected, unsigned int expectedsize)
{
	processTestInternal(key, keysize, iv, ivsize, input, inputsize, expected, expectedsize);
	processTestInternal(key, keysize, iv, ivsize, expected, expectedsize, input, inputsize);
}

